/**
  * @brief This function handles DMA1 channel2 global interrupt.
  */
void DMA1_Channel2_IRQHandler(void)
{
  /* USER CODE BEGIN DMA1_Channel2_IRQn 0 */
  	if (LL_DMA_IsActiveFlag_TC2(TMC_SPI_DMA))
	{
		LL_DMA_DisableChannel(TMC_SPI_DMA, TMC_SPI_DMA_RX_CH);
		LL_SPI_Disable(TMC_SPI);
//		LL_SPI_DisableDMAReq_RX(TMC_SPI);
		
		LL_DMA_ClearFlag_TC2(TMC_SPI_DMA);
	}
	else if (LL_DMA_IsActiveFlag_TE2(TMC_SPI_DMA))
	{
		SPI_TransferError_Callback();
	}

  /* USER CODE END DMA1_Channel2_IRQn 0 */
  /* USER CODE BEGIN DMA1_Channel2_IRQn 1 */

  /* USER CODE END DMA1_Channel2_IRQn 1 */
}

